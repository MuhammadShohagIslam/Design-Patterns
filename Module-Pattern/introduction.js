/*
    *** Module Pattern ***
        => Split up our code into smaller, reusable pieces.
        => It's allow our keep certain values within our file private.

        => Declarations within a module are scoped to that module.

        => If we do not explicity export a certain value,
        that value is not available outside that module.

    *** What is module ***
        => A module is a file containing JavaScript code, with some difference in behaviour compared to a normal sccript.
*/